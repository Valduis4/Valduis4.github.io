                        <p>Nvidia released the RTX A6000 in October 2020.</p>
<p>With a mind-blowing 48GB of VRAM and the Ampere architecture, this card is the best combined visualization &amp; compute hardware ever made in human history (at least as of today).</p>
<p>Some may say the A100 is faster - but it doesn&rsquo;t have raytracing. Some may say the 3090 has 1% faster ray-tracing performance - but it doesn&rsquo;t have the VRAM scale. The A6000 is really the top card for the frontier of combined visualization and compute at large scales.</p>
<p>Row64 works closely with NVIDIA and is constantly driving towards squeezing every drop of performance we can out of low-level hardware and software. So we were very excited to get ahold of this card.</p>
<p>When it arrived, it was so new that there was no branding on the box&hellip; just a plain white box with bubble wrap.<br>But inside was a RTX A6000!</p>
<p>Pretty exciting I was holding the most powerful graphics card in the world.</p>
<p><br><img src="https://row64.com/blog/images/A6000_Out_Of_Box.jpg"><br><br></p>
<p>It brought back a lot of memories&hellip; Back in the summer of 1993 I was an intern working for the Canadian Department of Public Works - inside their Digital Simulation Lab. This was one of the most cutting edge computing visualization research centers in all of Canada. I just got the job by chance, noticing it on the job board at University of Waterloo. That was the last time I had been so close to the top visualization power in the world.</p>
<p>Part of my job was helping purchase a million dollar computer&hellip; Who knew a computer could cost a million dollars Canadian? It was going to be used to model the parliament buildings of Canada and visualize new infrastructure phases over time.</p>
<p>The computer itself was called a &ldquo;SGI Onyx - Reality Engine 2&rdquo; and cost 500K.</p>
<p><br><img src="https://row64.com/blog/images/Compare_Size_03.jpg"><br><br></p>
<p>The RAM for the computer cost and additional 500K. There was so much RAM that a different word I had never heard of was used to describe it - a GIG.</p>
<p>We were paying 500K for 1 GIG of RAM. I told all my technical friends about it&hellip; everyone had the same response. &ldquo;What&rsquo;s a GIG?&rdquo;</p>
<p>Personally I just did little odd jobs around ordering the purchase. Coordinating the cooling for the room. Making sure the power was setup right. Answering questions about it when my boss was out. When it arrived I was not allowed to touch it or go near it. Only the top senior Graphics Operator could use it&hellip;</p>
<p>But out of the corner of my eye I was always watching what was happening on the screen. It was mind blowing.</p>
<p>Basically a 3D world could be navigated in real-time. A textured real world could be manipulated and moved around at 30 frames per second.</p>
<p>To me this was a total shock. I had some friends who did 3D renders on PC back then and they typically took 3 days to a week to render. Here it was happening as though time was nothing. Like the computer could see.</p>
<p><br><img src="https://row64.com/blog/images/Real_Time_Compare_01.jpg"><br><br></p>
<p>Looking down at this A6000 I could feel that something like this was happening again.</p>
<p>At Row64 we had been coding with real-time raytracing for a while at this point. To me, real-time raytracing is the only thing I&rsquo;ve seen in computer history that had this massive impact on that Reality Engine 2.</p>
<p>Real-time raytracing puts you into a visual world, but one far better than any traditional rasterization technology. It can be up to 2000X faster than CPU raytracing. So it has this same effect of drawing you into this new real-time visual experience.</p>
<p>It&rsquo;s also interesting to think about how much the graphics hardware has changed but is still kind of the same:</p>
<p><br><img src="https://row64.com/blog/images/Compare_Tech.jpg"><br><br></p>
<p><a href="https://slideplayer.com/slide/13036745/"><u>Here&rsquo;s a link</u></a> to an interesting deep dive into how graphics hardware has evolved over this 28-year span of time.</p>
<p>In general, both are using geometry engines or Graphics Processing layer that passes data to a rasterization layer.</p>
<p>The NVIDIA Ampere architecture however brings in many new core capabilities: CUDA Cores, Tensor Cores, and Ray Tracing Cores to name a few.</p>
<p>Let&rsquo;s look at the overall value change over this 28-year period:</p>
<p><br><img src="https://row64.com/blog/images/Compare_Speed_And_Cost_01.jpg"><br><br></p>
<p>Clearly, time travel forward in time is an outstanding strategy to get incredible graphics performance for a better price.</p>
<p>Note that the RTX speed of raytracing is at the same number of triangles but with raytracing instead of rasterization. A mind-blowing step forward in visualization quality&hellip; it&rsquo;s literally 1000X better. And keep in mind in a single frame in this example the A6000 is sending 8 rays and 16 bounces for every pixel on the screen - at 100fps.</p>
<p>Something interesting happened if you look back on the next step of the Reality Engine in history. SGI basically collapsed and the top brains working there went on to change the world of computers. Today&rsquo;s Supercomputer is tomorrow's computer, so people working in supercomputing get an early sense and experience of what the next generation of technology will look like. That&rsquo;s exactly how the Web Browser &amp; Graphics Card revolutions started.</p>
<p><br><img src="https://row64.com/blog/images/Supercomputing_Makes_The_Future.jpg"><br><br></p>
<p>So there you have it. We&rsquo;re living in a moment in time that is much like 1993. An incredible new technology &ndash; real-time raytracing &amp; compute has shattered the traditional barriers that were slowing down computers. Turning work that took weeks into milli-seconds.</p>
<p>It&rsquo;s an exciting moment and we can&rsquo;t wait to see what happens next. What new revolutions in computing will these new capabilities lead to?</p>
